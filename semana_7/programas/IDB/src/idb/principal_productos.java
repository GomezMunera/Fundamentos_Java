    private void botonEliminarFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarFActionPerformed
        int fila = TablaProductos.getSelectedRow();
        if(fila >=0 ){
            model.removeRow(fila);
            miTienda.inventario.remove(fila);
            int CantidadF = TablaProductos.getRowCount();
             for (int i=fila;i<CantidadF;i++){
                idP = i+1;
                model.setValueAt(String.valueOf(idP),i,0);
                
                Producto nuevo = new Producto(miTienda.inventario.get(i).nombre,
                        miTienda.inventario.get(i).stock,idP);
                
                miTienda.inventario.set(i,nuevo);
       
            }
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        }
    }//GEN-LAST:event_botonEliminarFActionPerformed

