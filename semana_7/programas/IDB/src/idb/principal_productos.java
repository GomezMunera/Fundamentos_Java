    private void botonAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarProductoActionPerformed
        
        idP += 1;
        String[] datos = new String[3];
        datos[0] = String.valueOf(idP);
        datos[1] = txtProducto.getText();
        datos[2] = "1000";
        datos[3] = txtStock.getText();
        
        if(!datos[2].isEmpty()){
            model.addRow(datos);
        }
        
        txtProducto.setText("");
        txtStock.setText("");
        
        if(datos[2].isEmpty()){
            JOptionPane.showMessageDialog(this, "Ingrese un valor para el stock",
                    "Error",0);

            idP -= 1;
        }

        // guardar en inventario
        Producto nuevo = new Producto(datos[1],Integer.parseInt(datos[2]),idP);
        miTienda.inventario.add(nuevo);
   
    }//GEN-LAST:event_botonAgregarProductoActionPerformed

