    private void botonVerStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVerStockActionPerformed
       
        // para borrar al principio
        int tamano = tablaVerStock.getRowCount();
        
        for(int i=tamano-1;i>=0;i--){
            model2.removeRow(i);
        }
        
        // recorrer inventario
        for(Producto item:miTienda.inventario){
            String[] datos = new String[3];
            datos[0] = String.valueOf(item.id);
            datos[1]= item.nombre;
            datos[2]= String.valueOf(item.stock);
            model2.addRow(datos);
        }

    }//GEN-LAST:event_botonVerStockActionPerformed

    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        String busqueda = txtBuscar.getText();

        // Busca únicamente comparando el nombre
        int indice = -1;
        for (int x = 0; x < miTienda.inventario.size(); x++) {
          Producto p = miTienda.inventario.get(x);
          if (p.nombre.equals(busqueda)) {
            indice = x;
            break; // Terminar ciclo si lo encuentra
          }
        }
        
        if (indice!=-1) {
            JOptionPane.showMessageDialog(this, "El producto "+ 
                    miTienda.inventario.get(indice).nombre 
                    +" esta cargado con id "
                    + String.valueOf(miTienda.inventario.get(indice).id)+
                    " y tiene un stock de "+
                    String.valueOf(miTienda.inventario.get(indice).stock),
                    "Encontrado",1);
	}else{
            JOptionPane.showMessageDialog(this, "El producto no se encontró",
                    "No encontrado",1);
	}
        
    }//GEN-LAST:event_botonBuscarActionPerformed

